#---------------------------------------------------------------------------------------
# This program finds the area of a triangle using the formula: area = 1/2*base*height
# Values are assigned to base and height
# Area is calculated and printed
#---------------------------------------------------------------------------------------

# assign the vlaue of 12 to base
base = 12

# assign the value of 16 to height
height = 16

# calculate area of a triangle using base and height
area = 0.5*base*height

# print the value of the area
print ('The area of a triangle with a base of', base, 'inches and height of', height, 'inches is', area, 'square inches.')
